//실습6 국어 시험 결시 학생 수 세기
#include <stdio.h>

int main()
{
	int score[10][3] = {
		{70,80,-1},{-1,90,75},{90,90,-1},
		{90,75,95},{90,80,80},{70,60,65},
		{65,85,95},{-1,85,90},{95,-1,95},
		{95,85,-1}
	};

	int count = 0;
	for (int i = 0; i < 10; i++)
	{
		if (score[i][0] < 0)
			count++;
	}

	printf("국어 시험 결시생은 %d명입니다.", count);

	return 0;

}
